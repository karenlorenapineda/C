#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*aux;

	if (!lst || !del)
		return ;
	aux = NULL;
	while (*lst)
	{
		aux = (*lst);
		*lst = aux->next;
		ft_lstdelone(aux, del);
	}
}